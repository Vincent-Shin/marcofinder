inserted = 0
updated = 0

for item in ITEMS:
    result = menu_items.update_one(
        {"unique_key": item["unique_key"]},
        {"$set": item},
        upsert=True,
    )
    if result.upserted_id:
        inserted += 1
    elif result.modified_count:
        updated += 1

