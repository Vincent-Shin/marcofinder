def build_item(
    *,
    restaurant_id,
    restaurant_name,
    item_name,
    category,
    price_cad,
    calories,
    protein_g,
    carbs_g,
    fat_g,
    sodium_mg,
    sugar_g,
    portion="1 item",
    description="",
    source_url="",
    price_source_url="",
):
    unique_key = f"{restaurant_id}|{item_name}|{portion}".lower()
    return {
        "restaurant_id": restaurant_id,
        "restaurant_name": restaurant_name,
        "item_name": item_name,
        "category": category,
        "portion": portion,
        "unique_key": unique_key,
        "price_cad": price_cad,
        "source_url": source_url,
        "price_source_url": price_source_url,
        "scraped_at": datetime.now(UTC).isoformat(),
        "description": description,
        "nutrition_invalid": False,
        "macros": {
            "calories": calories,
            "protein_g": protein_g,
            "carbs_g": carbs_g,
            "fat_g": fat_g,
            "sodium_mg": sodium_mg,
            "sugar_g": sugar_g,
        },
    }


